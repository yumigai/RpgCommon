    }

    private void Update() {
        if (Input.GetKeyDown(KeyCode.LeftArrow)) {
            pushNext(-1);
        }
        if (Input.GetKeyDown(KeyCode.RightArrow)) {
            pushNext(1);
        }
    }

    public void pushNext( int val ) {
        GameObject.Destroy(Camera.main.gameObject.GetComponent(Type.GetType(Filters[FilterIndex])));
        FilterIndex += val;
        FilterIndex = FilterIndex >= Filters.Length ? 0 : FilterIndex;
        FilterIndex = FilterIndex < 0 ? Filters.Length - 1 : FilterIndex;
        //FilterIndex = Mathf.Clamp(FilterIndex, 0, Filters.Length);
        Camera.main.gameObject.AddComponent(Type.GetType(Filters[FilterIndex]));
        Name.text = Filters[FilterIndex];
    }

